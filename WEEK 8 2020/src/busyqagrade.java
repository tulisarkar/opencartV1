
public class busyqagrade {

	public static void main(String[] args) {
		int marks=50;
		if(marks<=50){
		System.out.println("Failure");
		}
		else if(marks>50 && marks<=65){
		System.out.println("Average");
		}
		else if(marks>65){
		System.out.println("Excellent");
		}
		
		else{
		System.out.println("Invalid!");
	}
	}
}

